package com.remoer.member.ingredient.vo;

public class IngredientVO {
	
	private Long no;
	private String name, description, start_date, end_date;
	private Integer price, quantity;
	public Long getNo() {
		return no;
	}
	public void setNo(Long no) {
		this.no = no;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getStart_date() {
		return start_date;
	}
	public void setStart_date(String start_date) {
		this.start_date = start_date;
	}
	public String getEnd_date() {
		return end_date;
	}
	public void setEnd_date(String end_date) {
		this.end_date = end_date;
	}
	public Integer getPrice() {
		return price;
	}
	public void setPrice(Integer price) {
		this.price = price;
	}
	public Integer getQuantity() {
		return quantity;
	}
	public void setQuantity(Integer quantity) {
		this.quantity = quantity;
	}
	@Override
	public String toString() {
		return "IngredientVO [no=" + no + ", name=" + name + ", description=" + description + ", start_date="
				+ start_date + ", end_date=" + end_date + ", price=" + price + ", quantity=" + quantity + "]";
	}

	
}
